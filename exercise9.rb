#!/usr/bin/env ruby
# Programa que calcula potencias para un número dado
print "Escribe un número: "
num = gets.chomp.to_i
num2= num**2
num3= num**3
num4= num**4
num5= num**5
puts num.to_s + " ^ 2 = " + num2.to_s
puts num.to_s + " ^ 3 = " + num3.to_s
puts num.to_s + " ^ 4 = " + num4.to_s
puts num.to_s + " ^ 5 = " + num5.to_s
